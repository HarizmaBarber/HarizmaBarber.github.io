var i18n = {
  currentLang: localStorage.getItem('lang') || 'ru',

  translations: {},

  setLang: function(lang) {
    this.currentLang = lang;
    localStorage.setItem('lang', lang);
    this.translatePage();
    var switcher = document.getElementById('langSwitcher');
    if (switcher) {
      switcher.querySelectorAll('.lang-btn').forEach(function(btn) {
        btn.classList.toggle('active', btn.dataset.lang === lang);
      });
    }
  },

  get: function(key) {
    var t = this.translations[this.currentLang];
    return t && t[key] ? t[key] : (this.translations['ru'][key] || key);
  },

  translatePage: function() {
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
      var key = el.dataset.i18n;
      var attr = el.dataset.i18nAttr;
      if (attr) {
        el.setAttribute(attr, i18n.get(key));
      } else {
        el.textContent = i18n.get(key);
      }
    });
    document.documentElement.lang = this.currentLang === 'uz' ? 'uz' :
      this.currentLang === 'kz' ? 'kk' :
      this.currentLang === 'kg' ? 'ky' :
      this.currentLang === 'tj' ? 'tg' : 'ru';
  }
};

